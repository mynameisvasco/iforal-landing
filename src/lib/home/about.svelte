						Por que razão estudar forais medievais portugueses hoje?
					</h2>
					<p class=" lh-lg">
						Em primeiro lugar, existem razões de natureza eminentemente científica: o estudo dos
						forais antigos tem sido negligenciado em favor dos novos forais do século XVI (Reforma
						Manuelina), formal e materialmente mais atrativos para os historiadores; os estudos
						existentes ou se encontram demasiado dispersos por editoras com fraca capacidade de
						divulgação ou carecem de validação científica. Em qualquer dos casos, não existe uma
						perspetiva de conjunto que olhe para a totalidade dos forais régios.
					</p>
					<p class="lh-lg">
						Em segundo lugar, existem razões de natureza societal: em pleno século XXI, as
						comunidades locais e os poderes públicos (câmaras, arquivos, museus e escolas) continuam
						a comemorar a data de outorga do seu foral mais antigo, atualizando a memória e a
						identidade coletivas. O interesse pelo foral da terra justifica-se facilmente: nele, as
						comunidades conhecem as mais antigas formas de organização coletiva dos seus
						antepassados e a sua relação com os poderes instituídos, sobretudo com o rei, em
						questões de natureza política e fiscal, de justiça e de atividades económicas. Mais,
						estes documentos permitem ainda uma aproximação aos recursos naturais disponíveis na
						comunidade e à sua exploração, assim como à paisagem e à topografia, ainda reconhecíveis
						hoje pelas comunidades. Com este projeto, promove-se a salvaguarda de um dos mais
						antigos exemplos de património escrito dos poderes locais e também do Reino português.
					</p>
