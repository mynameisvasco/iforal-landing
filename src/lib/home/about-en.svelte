					<h2 class="mb-9 display-5 lh-sm font-heading">
						Why study Portuguese medieval forais today?
					</h2>
					<p class=" lh-lg">
						On one hand, we have eminently scientific reasons. First, research on old municipal
						charters (forais antigos) has been neglected, preference being given to the new
						municipal charters granted in the early 16th century (the Manueline Reform), which are
						formally and materially more attractive to the historians. Second, the existing studies
						are either scattered among publishers with weak dissemination capabilities or they lack
						scientific validation. In both cases, there is no global approach to the entire set of
						royal municipal charters.
					</p>
					<p class="lh-lg">
						On the other hand, we have societal reasons. In the 21st century, local communities and
						authorities (town halls, archives, museums and schools) keep on celebrating the date in
						which their oldest municipal charter was granted, updating collective memory and
						identity. Community interest in the local foral can be easily understood; the charter
						contains the oldest forms of collective organization of people’s ancestors and their
						relationship with the established powers – namely the King, in matters pertaining to
						politics, taxation, justice and economic activity. Moreover, the forais enable people to
						know the natural resources then available to the community and their exploitation, as
						well as the landscape and the topography – still perceivable by the communities today.
						Our project aims to promote the safeguard of one of the oldest collections of written
						heritage of local communities and also of Portuguese kingdom.
					</p>
